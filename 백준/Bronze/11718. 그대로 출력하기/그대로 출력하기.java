import java.util.*;
import java.lang.*;
import java.io.*;

// 백준 11718
class Main {

    public static void main(String[] args) throws IOException {
        
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));

        String str;

        while((str = br.readLine()) != null){
            System.out.println(str);
        }

        br.close();
    }
}